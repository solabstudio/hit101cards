let current: Locale = (() => {
  try {
    const saved = localStorage.getItem(KEY);
    if (saved === 'ja' || saved === 'en') return saved;
  } catch {}
  if (typeof navigator !== 'undefined' && navigator.language && !navigator.language.startsWith('ja')) {
    return 'en';
  }
  return 'ja';
})();

const listeners = new Set<() => void>();

export function getLocale(): Locale {
  return current;
}

export function setLocale(loc: Locale) {
  if (loc === current) return;
  current = loc;
  try { localStorage.setItem(KEY, loc); } catch {}
  for (const l of listeners) l();
}

export function useLocale(): [Locale, (l: Locale) => void] {
  const [, force] = useState(0);
  useEffect(() => {
    const cb = () => force((n) => n + 1);
    listeners.add(cb);
    return () => { listeners.delete(cb); };
  }, []);
  return [current, setLocale];
}

// 翻訳キーを取得 (プレースホルダ {key} は params で置換)
export function t(key: string, params?: Record<string, string | number>): string {
  const dict = STRINGS[current] || STRINGS.ja;
  let str = dict[key];
  if (str === undefined) {
    // フォールバック: ja → en の順
    str = STRINGS.ja[key] ?? STRINGS.en[key] ?? key;
  }
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return str;
}
